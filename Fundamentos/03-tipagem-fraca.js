// Tipagem Fraca

//typeof mostra o tipo que está na variável.

let qualquer = 'legal';
console.log(qualquer);
console.log(typeof qualquer);

qualquer = 3.1516;
console.log(qualquer);
console.log(typeof qualquer);

// a mesma variável pode ter varios tipos de valores, isso o faz uma linguagem de tipagem fraca.
